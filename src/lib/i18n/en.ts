// Recursively widen string literal types to string so other locales can have different values
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string ? string : DeepStringify<T[K]>;
};

// Label maps (statusLabels, sourceLabels, etc.) need to accept arbitrary string keys
// because Prisma enum values come as `string` at runtime
type WithStringIndex<T> = T & Record<string, string>;

type BaseTranslation = DeepStringify<typeof en>;

export type TranslationKeys = Omit<BaseTranslation, 'statusLabels' | 'sourceLabels' | 'serviceCategoryLabels' | 'channelLabels' | 'outcomeLabels' | 'providerStatusLabels'> & {
  statusLabels: WithStringIndex<BaseTranslation['statusLabels']>;
  sourceLabels: WithStringIndex<BaseTranslation['sourceLabels']>;
  serviceCategoryLabels: WithStringIndex<BaseTranslation['serviceCategoryLabels']>;
  channelLabels: WithStringIndex<BaseTranslation['channelLabels']>;
  outcomeLabels: WithStringIndex<BaseTranslation['outcomeLabels']>;
  providerStatusLabels: WithStringIndex<BaseTranslation['providerStatusLabels']>;
};

export default en;
